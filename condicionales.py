#Sentencia donde se debe cumplir una condicion
#Sintaxis if 
#Ej. Verficar entre 2 numeros cual es el mayor
numero1=int(input("Ingrese un numero ")) 
numero2=int(input("Ingrese un numero "))
if numero1>numero2:
    print("El mayor es ",numero1)
elif numero1==numero2:
    print("Los numeros son igual")
else:
    print("El mayor es ",numero2)

